package edu.supercom.util;

import java.util.Map;

/**
 * A simple map is a map with only the {@link Map#get(Object)} method.  
 * It can also be see as a function from {@link K} to {@link V}.  
 * 
 * @param <K> the type of the key.  
 * @param <V> the type of the value.  
 * */
public interface SimpleMap<K,V> {

	/**
	 * Returns the value associated with the specified key.  
	 * 
	 * @param k the key.
	 * @return the value associated with <code>k</code>.  
	 * */
	public V get(K k);
}
