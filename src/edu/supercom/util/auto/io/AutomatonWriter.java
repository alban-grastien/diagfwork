package edu.supercom.util.auto.io;

import edu.supercom.util.auto.ReadableAutomaton;

/**
 * An <b>automaton writer</b> is an object that is able to return a string
 * representation of an automaton.  
 *
 * @author Alban Grastien
 */
public interface AutomatonWriter {

    /**
     * Returns a string representation of the specified automaton.
     *
     * @param auto the automaton of which a string representation is expected.  
     */
    public String toString(ReadableAutomaton auto);

}
