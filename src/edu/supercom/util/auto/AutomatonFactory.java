package edu.supercom.util.auto;

/**
 * An <b>automaton factory</b> is an object that is able to build a new
 * automaton.
 *
 * @author Alban Grastien
 */
public interface AutomatonFactory {

    /**
     * Creates a new automaton with the specified trans label as empty trans
     * label.
     *
     * @param tl the empty trans label.
     */
    public <SL,TL>
            Automaton<SL,TL> buildAutomaton(TL tl);

}
