package edu.supercom.util.auto.label;

/**
 * A <b>fake label</b> is a label that does not provide any information.  It
 * should be used to give a simple name to a complex state or transition label
 * for which the label is not really relevant.  For instance, if you have a
 * Automaton<A<B,C<D>>,E> that you want to synchronise with another automaton,
 * and if the synchronisation is only on the transition label while the state
 * label does not provide information, then the automaton can be casted into
 * Automaton<FakeLabel,E>.  A fake label cannot be instanciated.  
 *
 * @author alban
 */
public class FakeLabel {

    private FakeLabel() {}

}
