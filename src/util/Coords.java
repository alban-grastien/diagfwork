package util;

public class Coords 
{
	private int x, y;

	public Coords()
	{
		x = 0; y = 0;
	}
	
	public Coords(int x_new, int y_new)
	{
		x = x_new; y = y_new;
	}
	
	public void setX(int x) 
	{
		this.x = x;
	}

	public int x() 
	{
		return x;
	}

	public void setY(int y) 
	{
		this.y = y;
	}

	public int y() 
	{
		return y;
	}
}
