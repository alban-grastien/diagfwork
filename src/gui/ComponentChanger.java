package gui;

/**
 * A <code>ComponentChanger</code>, i.e., a component changer, 
 * is an element of the GUI 
 * who is in charge of warning other elements that the component 
 * currently selected has changed.  
 */
public interface ComponentChanger {
    
    /**
     * Adds the specified component listener 
     * to the list of component listeners 
     * that should be warned when the selected component changes.  
     * 
     * @param cl the component listener to add to the list.  
     */
    public void addComponentListener(ComponentListener cl);
    
    /**
     * Removes the specified component listener 
     * from the list of component listeners 
     * that should be warned when the selected component changes.  
     * 
     * @param cl the component listener that no longer needs to be notified 
     * when the selected component changes.  
     */
    public void removeComponentListener(ComponentListener cl);
    
}
